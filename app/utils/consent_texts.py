DEFAULT_LANGUAGE = "en"

def get_consent_text(language_code: str) -> str:
    """Get consent text for the specified language"""
    return CONSENT_TEXTS.get(language_code, CONSENT_TEXTS[DEFAULT_LANGUAGE])["consent_text"]

def get_consent_data(language_code: str) -> dict:
    """Get all consent data for the specified language"""
    return CONSENT_TEXTS.get(language_code, CONSENT_TEXTS[DEFAULT_LANGUAGE])

def is_consent_yes(message: str, language_code: str) -> bool:
    """Check if message indicates consent approval"""
    data = get_consent_data(language_code)
    return message.upper().strip() in [kw.upper() for kw in data["yes_keywords"]]

def is_consent_no(message: str, language_code: str) -> bool:
    """Check if message indicates consent denial"""
    data = get_consent_data(language_code)
    return message.upper().strip() in [kw.upper() for kw in data["no_keywords"]]

def is_delete_request(message: str, language_code: str) -> bool:
    """Check if message is a delete request"""
    data = get_consent_data(language_code)
    return message.upper().strip() == data["delete_keyword"].upper()

